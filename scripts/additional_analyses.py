def _temperature_scale(prob: np.ndarray, temperature: float) -> np.ndarray:
    prob = np.clip(prob, 1e-12, 1.0)
    logits = np.log(prob) / float(temperature)
    logits -= logits.max(axis=1, keepdims=True)
    ex = np.exp(logits)
    return ex / ex.sum(axis=1, keepdims=True)


def ece_score_with_fixed_pred(y: np.ndarray, prob: np.ndarray, pred: np.ndarray, n_bins: int = 10) -> float:
    """Label-aware ECE using the probability assigned to the issued label.

    This is the primary ECE for label-preserving calibration controls. It avoids
    mixing an unchanged issued label with the calibrated probability argmax when
    post-hoc calibration changes the highest-probability class.
    """
    pred = np.asarray(pred).astype(int)
    y = np.asarray(y).astype(int)
    conf = np.clip(prob[np.arange(len(y)), pred], 0.0, 1.0)
    correct = (pred == y).astype(float)
    bins = np.linspace(0, 1, n_bins + 1)
    ece = 0.0
    for lo, hi in zip(bins[:-1], bins[1:]):
        mask = (conf > lo) & (conf <= hi)
        if mask.any():
            ece += float(mask.mean() * abs(correct[mask].mean() - conf[mask].mean()))
    return float(ece)


def adaptive_ece_score(y: np.ndarray, prob: np.ndarray, pred: np.ndarray | None = None, n_bins: int = 10) -> float:
    """ECE with equal-frequency confidence bins.

    When ``pred`` is provided, confidence is the probability assigned to that
    fixed issued label; otherwise the calibrated probability argmax is used.
    """
    if pred is None:
        pred = prob.argmax(axis=1)
        conf = prob.max(axis=1)
    else:
        pred = np.asarray(pred).astype(int)
        conf = np.clip(prob[np.arange(len(y)), pred], 0.0, 1.0)
    correct = (pred == y).astype(float)
    order = np.argsort(conf)
    splits = np.array_split(order, n_bins)
    ece = 0.0
    n = len(y)
    for idx in splits:
        if len(idx) == 0:
            continue
        ece += (len(idx) / n) * abs(float(correct[idx].mean()) - float(conf[idx].mean()))
    return float(ece)


def classwise_ece_score(y: np.ndarray, prob: np.ndarray, n_bins: int = 10) -> float:
    """Mean one-vs-rest ECE across classes."""
    vals = []
    bins = np.linspace(0, 1, n_bins + 1)
    for c in range(prob.shape[1]):
        p = prob[:, c]
        observed = (y == c).astype(float)
        ece = 0.0
        for lo, hi in zip(bins[:-1], bins[1:]):
            mask = (p > lo) & (p <= hi)
            if mask.any():
                ece += float(mask.mean() * abs(observed[mask].mean() - p[mask].mean()))
        vals.append(ece)
    return float(np.mean(vals))


def multiclass_nll(y: np.ndarray, prob: np.ndarray) -> float:
    labels = list(range(prob.shape[1]))
    return float(log_loss(y, np.clip(prob, 1e-12, 1.0), labels=labels))


def _metric_bundle(ytrue: pd.DataFrame, pred: Dict[str, np.ndarray], prob: Dict[str, np.ndarray]) -> Dict[str, float]:
    cfg = load_config()
    rows = []
    for sc in SCALE_ORDER:
        y = ytrue[f"{sc}_severity_idx"].astype(int).values
        p = np.asarray(pred[sc]).astype(int)
        pr = np.asarray(prob[sc], dtype=float)
        high = cfg["labels"][sc]["high_risk_index"]
        eye = np.eye(pr.shape[1])
        argmax_pred = pr.argmax(axis=1)
        rows.append({
            "scale": sc,
            "macro_f1": float(f1_score(y, p, average="macro", zero_division=0)),
            "mae": float(mean_absolute_error(y, p)),
            "high_risk_recall": float(recall_score(y >= high, p >= high, zero_division=0)),
            "brier_score": float(np.mean(np.sum((pr - eye[y]) ** 2, axis=1))),
            # Primary ECE: confidence assigned to the issued label.
            "ece": ece_score_with_fixed_pred(y, pr, p),
            "ece_argmax": ece_score(y, pr),
            "adaptive_ece": adaptive_ece_score(y, pr, p),
            "adaptive_ece_argmax": adaptive_ece_score(y, pr),
            "classwise_ece": classwise_ece_score(y, pr),
            "nll": multiclass_nll(y, pr),
            "argmax_change_rate": float(np.mean(argmax_pred != p)),
        })
    df = pd.DataFrame(rows)
    return {f"mean_{c}": float(df[c].mean()) for c in [
        "macro_f1", "mae", "high_risk_recall", "brier_score", "ece",
        "ece_argmax", "adaptive_ece", "adaptive_ece_argmax", "classwise_ece",
        "nll", "argmax_change_rate"
    ]}




def _bootstrap_calibration_deltas(
    ytrue: pd.DataFrame,
    method_payloads: Dict[str, Tuple[Dict[str, np.ndarray], Dict[str, np.ndarray]]],
    n_boot: int = 200,
) -> pd.DataFrame:
    """Participant-level bootstrap CIs for label-aware ECE deltas."""
    rng = np.random.default_rng(load_config()["seed"] + 2027)
    n = len(ytrue)
    comparisons = [
        ("true_rt_calibration_only_minus_response_only", "true_rt_calibration_only", "response_only_uncalibrated"),
        ("constant_softening_minus_response_only", "constant_softening", "response_only_uncalibrated"),
        ("temperature_scaling_minus_response_only", "temperature_scaling", "response_only_uncalibrated"),
        ("one_vs_rest_isotonic_minus_response_only", "one_vs_rest_isotonic", "response_only_uncalibrated"),
        ("true_rt_calibration_only_minus_constant_softening", "true_rt_calibration_only", "constant_softening"),
        ("true_rt_calibration_only_minus_isotonic", "true_rt_calibration_only", "one_vs_rest_isotonic"),
    ]

    def mean_ece_for(method: str, idx: np.ndarray) -> float:
        pred, prob = method_payloads[method]
        vals = []
        for sc in SCALE_ORDER:
            y = ytrue[f"{sc}_severity_idx"].astype(int).values[idx]
            vals.append(ece_score_with_fixed_pred(y, prob[sc][idx], pred[sc][idx]))
        return float(np.mean(vals))

    rows = []
    for label, a, b in comparisons:
        deltas = []
        for _ in range(n_boot):
            idx = rng.integers(0, n, n)
            deltas.append(mean_ece_for(a, idx) - mean_ece_for(b, idx))
        arr = np.asarray(deltas, dtype=float)
        rows.append({
            "comparison": label,
            "metric": "mean_label_aware_ece_delta",
            "delta_mean": float(arr.mean()),
            "ci_lower_2p5": float(np.quantile(arr, 0.025)),
            "ci_upper_97p5": float(np.quantile(arr, 0.975)),
            "bootstrap_repeats": int(n_boot),
        })
    out = pd.DataFrame(rows)
    out.to_csv(TABLE_DIR / "table13_calibration_delta_bootstrap_19.csv", index=False)
    return out


def _fast_response_policy_selection(policy: str, budget: int, ids: Iterable[int], long: pd.DataFrame, seed: int) -> Dict[int, List[str]]:
    """Vectorized equivalent of response/RT-aware uncertainty item selection.

    The main engine keeps a transparent reference implementation. This helper is
    used only for the additional 19-item sensitivity analyses so the compact
    release can recompute reviewer-requested checks without repeatedly looping
    over participants in Python. It preserves the selected item set used by the
    reference policy; prediction functions depend on the selected set rather
    than on reveal order.
    """
    if policy not in {"response_only_uncertainty", "rt_aware_uncertainty", "rt_only_uncertainty"}:
        return simulate_item_selection(policy, budget, ids, long=long, seed=seed)
    ids = list(map(int, ids))
    use_rt = policy in {"rt_aware_uncertainty", "rt_only_uncertainty"}
    info = item_info_values(long)
    info_map = dict(zip(info["global_item_id"], info["rank_score"]))
    info_scores = np.array([info_map.get(item, 0.0) for item in ITEM_ORDER], dtype=float)
    top_by_scale = {sc: info[info["scale"] == sc]["global_item_id"].tolist() for sc in SCALE_ORDER}
    warm_idx = []
    for sc in SCALE_ORDER:
        if top_by_scale[sc]:
            warm_idx.append(ITEM_ORDER.index(top_by_scale[sc][0]))
    warm_idx = warm_idx[:min(len(warm_idx), budget)]
    subset = long[long["participant_id"].isin(ids)]
    resp_mat = subset.pivot(index="participant_id", columns="global_item_id", values="item_response").reindex(index=ids, columns=ITEM_ORDER).to_numpy(dtype=float)
    resid_mat = subset.pivot(index="participant_id", columns="global_item_id", values="rt_residual_content_z").reindex(index=ids, columns=ITEM_ORDER).to_numpy(dtype=float)
    item_scale_idx = np.array([SCALE_ORDER.index(ITEM_TO_SCALE[item]) for item in ITEM_ORDER])
    scale_item_masks = np.stack([item_scale_idx == si for si in range(len(SCALE_ORDER))], axis=0)
    cutoffs = {sc: np.array(load_config()["labels"][sc]["cutoffs"][1:-1], dtype=float) for sc in SCALE_ORDER}
    selected_mask = np.zeros((len(ids), len(ITEM_ORDER)), dtype=bool)
    selected_mask[:, warm_idx] = True
    pid_arr = np.array(ids, dtype=int)
    while selected_mask.sum(axis=1).min() < budget:
        counts = selected_mask.astype(float) @ scale_item_masks.T.astype(float)
        scale_unc = np.ones((len(ids), len(SCALE_ORDER)), dtype=float) * 2.0
        for si, sc in enumerate(SCALE_ORDER):
            mask = selected_mask & scale_item_masks[si]
            cnt = counts[:, si]
            has = cnt > 0
            vals = np.where(mask, resp_mat, 0.0)
            s_resp = vals.sum(axis=1)
            mean = np.zeros(len(ids), dtype=float)
            mean[has] = s_resp[has] / cnt[has]
            ss_resp = np.where(mask, resp_mat ** 2, 0.0).sum(axis=1)
            var = np.zeros(len(ids), dtype=float)
            var[has] = np.maximum(ss_resp[has] / cnt[has] - mean[has] ** 2, 0.0)
            std = np.sqrt(var)
            est = mean * SCALES[sc] - (14 if sc == "pss" else 0)
            boundaries = cutoffs[sc]
            dist = np.min(np.abs(est[:, None] - boundaries[None, :]), axis=1) if len(boundaries) else np.full(len(ids), 3.0)
            unc = (1.0 / (1.0 + dist)) + (1.0 - cnt / SCALES[sc]) + 0.10 * std
            scale_unc[has, si] = unc[has]
        reliability = np.ones(len(ids), dtype=float)
        if use_rt:
            cnt_all = selected_mask.sum(axis=1).astype(float)
            fast = ((resid_mat < -1) & selected_mask).sum(axis=1) / cnt_all
            long_pause = ((resid_mat > 1) & selected_mask).sum(axis=1) / cnt_all
            vals = np.where(selected_mask, resid_mat, 0.0)
            mean = vals.sum(axis=1) / cnt_all
            ss = np.where(selected_mask, resid_mat ** 2, 0.0).sum(axis=1)
            std = np.sqrt(np.maximum(ss / cnt_all - mean ** 2, 0.0))
            reliability = np.clip(1.0 - 0.35 * fast - 0.20 * long_pause - 0.08 * np.minimum(std, 4.0), 0.10, 1.0)
            scale_unc *= (1.0 + 0.25 * np.maximum(0.0, 1.0 - reliability))[:, None]
        unreliability = np.maximum(0.0, 1.0 - reliability)
        counts_min = counts.min(axis=1)
        scores = np.empty((len(ids), len(ITEM_ORDER)), dtype=float)
        for j in range(len(ITEM_ORDER)):
            si = item_scale_idx[j]
            coverage_gap = 1.0 - counts[:, si] / SCALES[SCALE_ORDER[si]]
            undercovered = (counts[:, si] <= counts_min).astype(float)
            lambda_t = 0.20 + (0.40 * unreliability if use_rt else 0.0)
            rho_t = 0.015 + (0.020 * unreliability if use_rt else 0.0)
            eta_t = 0.00 + (0.25 * unreliability if use_rt else 0.0)
            item_scores = info_scores[j] * (1.0 + scale_unc[:, si])
            item_scores += lambda_t * coverage_gap + 0.08 * undercovered
            item_scores += eta_t * info_scores[j] * (1.0 + undercovered)
            item_scores -= rho_t * counts[:, si]
            item_scores += ((pid_arr * (j + 7)) % 19) * 1e-6
            scores[:, j] = item_scores
        scores[selected_mask] = -1e12
        next_idx = scores.argmax(axis=1)
        selected_mask[np.arange(len(ids)), next_idx] = True
    return {int(pid): [ITEM_ORDER[i] for i in np.where(selected_mask[r])[0]][:budget] for r, pid in enumerate(ids)}

