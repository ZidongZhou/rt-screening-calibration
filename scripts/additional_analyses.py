def _fit_content_expected(train: pd.DataFrame, apply: pd.DataFrame, lower_pct: float, upper_pct: float, fixed: str) -> Tuple[np.ndarray, np.ndarray, float, float]:
    tr = train.copy(); ap = apply.copy()
    lo, hi = np.percentile(tr["raw_rt"].astype(float), [lower_pct, upper_pct])
    tr["log_rt_tmp"] = np.log1p(tr["raw_rt"].clip(lo, hi))
    ap["log_rt_tmp"] = np.log1p(ap["raw_rt"].clip(lo, hi))
    if fixed == "content":
        Xtr = pd.get_dummies(tr[["word_count", "within_scale_position", "scale"]], columns=["scale"], drop_first=False)
        Xap = pd.get_dummies(ap[["word_count", "within_scale_position", "scale"]], columns=["scale"], drop_first=False).reindex(columns=Xtr.columns, fill_value=0)
        model = LinearRegression().fit(Xtr, tr["log_rt_tmp"])
        exp_tr = model.predict(Xtr); exp_ap = model.predict(Xap)
    elif fixed == "item_fixed":
        means = tr.groupby("global_item_id")["log_rt_tmp"].mean()
        global_mean = float(tr["log_rt_tmp"].mean())
        exp_tr = tr["global_item_id"].map(means).fillna(global_mean).values
        exp_ap = ap["global_item_id"].map(means).fillna(global_mean).values
    elif fixed == "scale_specific":
        exp_tr = np.zeros(len(tr)); exp_ap = np.zeros(len(ap))
        for sc in SCALE_ORDER:
            tr_idx = tr["scale"].values == sc
            ap_idx = ap["scale"].values == sc
            model = LinearRegression().fit(tr.loc[tr_idx, ["word_count", "within_scale_position"]], tr.loc[tr_idx, "log_rt_tmp"])
            exp_tr[tr_idx] = model.predict(tr.loc[tr_idx, ["word_count", "within_scale_position"]])
            exp_ap[ap_idx] = model.predict(ap.loc[ap_idx, ["word_count", "within_scale_position"]])
    else:
        raise ValueError(fixed)
    resid_tr = tr["log_rt_tmp"].values - exp_tr
    mean = float(resid_tr.mean()); sd = float(resid_tr.std(ddof=0) or 1.0)
    z_ap = (ap["log_rt_tmp"].values - exp_ap - mean) / sd
    r2_tr = float(1 - np.sum((tr["log_rt_tmp"].values - exp_tr) ** 2) / np.sum((tr["log_rt_tmp"].values - tr["log_rt_tmp"].mean()) ** 2))
    return z_ap, exp_ap, r2_tr, sd


