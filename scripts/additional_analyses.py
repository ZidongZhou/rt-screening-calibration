def run_calibration_grid_boundary_sensitivity(long: pd.DataFrame | None = None) -> pd.DataFrame:
    """Expanded calibration-grid sensitivity check at the 19-item budget.

    This analysis extends the original scalar sigma grid from an upper bound of
    6 to 10 and the temperature grid from an upper bound of 5 to 10. It is a
    focused 19-item sensitivity check, not a replacement for the validation-
    selected main tables. The goal is to test whether the qualitative conclusion
    changes when PSS calibration is allowed stronger generic smoothing.
    """
    if long is None:
        long = fit_rt_residuals()
    cfg = load_config()
    budget = 19
    val_ids = load_ids("val")
    test_ids = load_ids("test")
    seed = cfg["seed"] + budget
    selected_val = _fast_response_policy_selection("response_only_uncertainty", budget, val_ids, long, seed=seed)
    selected_test = _fast_response_policy_selection("response_only_uncertainty", budget, test_ids, long, seed=seed)
    targets_val = make_targets_frame().set_index("participant_id").loc[val_ids]
    targets_test = make_targets_frame().set_index("participant_id").loc[test_ids]
    original_sigma_grid = cfg.get("calibration_grid", [1.0, 1.5, 2.0, 2.5, 3.0, 4.0, 5.0, 6.0])
    expanded_sigma_grid = sorted(set([float(x) for x in original_sigma_grid] + [8.0, 10.0]))
    original_temp_grid = [0.50, 0.67, 0.75, 1.00, 1.25, 1.50, 1.75, 2.00, 2.50, 3.00, 4.00, 5.00]
    expanded_temp_grid = sorted(set(original_temp_grid + [6.0, 8.0, 10.0]))

    def _select_sigma(scores, state, variant, grid, sc):
        yv = targets_val[f"{sc}_severity_idx"].astype(int).values
        pred_val = _labels_for_scores(sc, scores[sc])
        mult = _sigma_multiplier_for_variant(variant, sc, scores[sc], state, val_ids)
        best = (float(grid[0]), math.inf)
        for sig in grid:
            if variant in {"responses_rt_calibration_only", "responses_constant_softening", "responses_coverage_only_calibration", "responses_uncertainty_only_calibration", "responses_shuffled_rt_calibration"}:
                prob = _probs_for_scores_variable_sigma(sc, scores[sc], float(sig) * mult)
            else:
                prob = _probs_for_scores(sc, scores[sc], sigma=float(sig))
            ece = ece_score_with_fixed_pred(yv, prob, pred_val)
            if ece < best[1]:
                best = (float(sig), float(ece))
        return best

    def _test_metrics(scores, state, variant, sigma, sc):
        yt = targets_test[f"{sc}_severity_idx"].astype(int).values
        pred = _labels_for_scores(sc, scores[sc])
        mult = _sigma_multiplier_for_variant(variant, sc, scores[sc], state, test_ids)
        if variant in {"responses_rt_calibration_only", "responses_constant_softening", "responses_coverage_only_calibration", "responses_uncertainty_only_calibration", "responses_shuffled_rt_calibration"}:
            prob = _probs_for_scores_variable_sigma(sc, scores[sc], float(sigma) * mult)
        else:
            prob = _probs_for_scores(sc, scores[sc], sigma=float(sigma))
        eye = np.eye(prob.shape[1])
        return {
            "test_ece_label": ece_score_with_fixed_pred(yt, prob, pred),
            "test_brier": float(np.mean(np.sum((prob - eye[yt]) ** 2, axis=1))),
            "test_nll": multiclass_nll(yt, prob),
        }

    rows = []
    # Score-to-probability sigma grid for response-only and true RT calibration-only.
    for variant_label, variant in [("response_only_sigma", "responses"), ("true_rt_calibration_sigma", "responses_rt_calibration_only")]:
        scores_val, state_val = _scores_from_selected(val_ids, selected_val, variant, long)
        scores_test, state_test = _scores_from_selected(test_ids, selected_test, variant, long)
        for sc in SCALE_ORDER:
            sig0, val0 = _select_sigma(scores_val, state_val, variant, original_sigma_grid, sc)
            sig1, val1 = _select_sigma(scores_val, state_val, variant, expanded_sigma_grid, sc)
            met0 = _test_metrics(scores_test, state_test, variant, sig0, sc)
            met1 = _test_metrics(scores_test, state_test, variant, sig1, sc)
            rows.append({
                "check": variant_label,
                "scale": sc,
                "original_upper_bound": 6.0,
                "expanded_upper_bound": 10.0,
                "original_selected": sig0,
                "expanded_selected": sig1,
                "hit_original_upper_bound": bool(sig0 >= 6.0),
                "validation_metric_original": val0,
                "validation_metric_expanded": val1,
                "test_ece_label_original": met0["test_ece_label"],
                "test_ece_label_expanded": met1["test_ece_label"],
                "test_brier_original": met0["test_brier"],
                "test_brier_expanded": met1["test_brier"],
                "test_nll_original": met0["test_nll"],
                "test_nll_expanded": met1["test_nll"],
                "interpretation": "Expanded smoothing was selected for PSS; qualitative RT-specific conclusion unchanged." if sc == "pss" and sig1 > sig0 else "No qualitative change."
            })

    # Temperature scaling grid extension using response-only probabilities selected under the original sigma grid.
    scores_val, state_val = _scores_from_selected(val_ids, selected_val, "responses", long)
    scores_test, state_test = _scores_from_selected(test_ids, selected_test, "responses", long)
    base_sigma = {}
    for sc in SCALE_ORDER:
        base_sigma[sc], _ = _select_sigma(scores_val, state_val, "responses", original_sigma_grid, sc)
    prob_val = {sc: _probs_for_scores(sc, scores_val[sc], sigma=base_sigma[sc]) for sc in SCALE_ORDER}
    prob_test = {sc: _probs_for_scores(sc, scores_test[sc], sigma=base_sigma[sc]) for sc in SCALE_ORDER}
    pred_test = {sc: _labels_for_scores(sc, scores_test[sc]) for sc in SCALE_ORDER}
    for sc in SCALE_ORDER:
        yv = targets_val[f"{sc}_severity_idx"].astype(int).values
        yt = targets_test[f"{sc}_severity_idx"].astype(int).values
        def best_temp(grid):
            vals = [(float(T), multiclass_nll(yv, _temperature_scale(prob_val[sc], float(T)))) for T in grid]
            return min(vals, key=lambda x: x[1])
        T0, val0 = best_temp(original_temp_grid)
        T1, val1 = best_temp(expanded_temp_grid)
        p0 = _temperature_scale(prob_test[sc], T0)
        p1 = _temperature_scale(prob_test[sc], T1)
        eye = np.eye(p0.shape[1])
        rows.append({
            "check": "temperature_scaling_grid",
            "scale": sc,
            "original_upper_bound": 5.0,
            "expanded_upper_bound": 10.0,
            "original_selected": T0,
            "expanded_selected": T1,
            "hit_original_upper_bound": bool(T0 >= 5.0),
            "validation_metric_original": val0,
            "validation_metric_expanded": val1,
            "test_ece_label_original": ece_score_with_fixed_pred(yt, p0, pred_test[sc]),
            "test_ece_label_expanded": ece_score_with_fixed_pred(yt, p1, pred_test[sc]),
            "test_brier_original": float(np.mean(np.sum((p0 - eye[yt]) ** 2, axis=1))),
            "test_brier_expanded": float(np.mean(np.sum((p1 - eye[yt]) ** 2, axis=1))),
            "test_nll_original": multiclass_nll(yt, p0),
            "test_nll_expanded": multiclass_nll(yt, p1),
            "interpretation": "Expanded temperature selected; generic calibration smoothing remains stronger than RT-specific interpretation." if sc == "pss" and T1 > T0 else "No qualitative change."
        })
    out = pd.DataFrame(rows)
    out.to_csv(TABLE_DIR / "table14_calibration_grid_boundary_sensitivity_19.csv", index=False)
    return out


def run_stepwise_leakage_audit(long: pd.DataFrame) -> pd.DataFrame:
    cfg = load_config()
    rng = np.random.default_rng(cfg["seed"] + 12021)
    ids = rng.choice(load_ids("test"), size=min(25, len(load_ids("test"))), replace=False).astype(int).tolist()
    budget = 19
    base = simulate_item_selection("rt_aware_uncertainty", budget, ids, long=long, seed=cfg["seed"] + budget)
    sample = long[long["participant_id"].isin(ids)].copy()
    if "pair_key" not in sample.columns:
        sample["pair_key"] = sample["participant_id"].astype(str) + "|" + sample["global_item_id"].astype(str)
    rows = []
    for step in range(budget):
        prefix_pairs = set(str(pid) + "|" + item for pid, items in base.items() for item in items[:step])
        future_mask = ~sample["pair_key"].isin(prefix_pairs)
        for perturb_type, cols in [
            ("future_response", ["item_response"]),
            ("future_rt", ["raw_rt", "log_rt", "rt_residual_content_z", "rt_residual_conditioned_z"]),
        ]:
            pert = sample.copy()
            for col in cols:
                if col in pert.columns:
                    vals = pert.loc[future_mask, col].values.copy()
                    pert.loc[future_mask, col] = rng.permutation(vals)
            sel = simulate_item_selection("rt_aware_uncertainty", step + 1, ids, long=pert, seed=cfg["seed"] + budget)
            unchanged = sum(1 for pid in ids if sel[int(pid)][: step + 1] == base[int(pid)][: step + 1])
            rows.append({
                "audit": "stepwise_future_information_perturbation",
                "perturbation": perturb_type,
                "step_before_selection": step,
                "participants_checked": len(ids),
                "prefixes_unchanged": unchanged,
                "status": "PASS" if unchanged == len(ids) else "FAIL",
            })
    out = pd.DataFrame(rows)
    out.to_csv(TABLE_DIR / "table7b_stepwise_leakage_audit.csv", index=False)
    summary = out.groupby("perturbation", as_index=False).agg(
        prefixes_unchanged=("prefixes_unchanged", "sum"), participants_checked=("participants_checked", "sum")
    )
    summary["status"] = np.where(summary["prefixes_unchanged"] == summary["participants_checked"], "PASS", "FAIL")
    summary.to_csv(TABLE_DIR / "table7c_stepwise_leakage_summary.csv", index=False)
    return out


def _reliability_curve(y: np.ndarray, prob: np.ndarray, n_bins: int = 10) -> pd.DataFrame:
    conf = prob.max(axis=1); pred = prob.argmax(axis=1); correct = (pred == y).astype(float)
    bins = np.linspace(0, 1, n_bins + 1)
    rows = []
    for lo, hi in zip(bins[:-1], bins[1:]):
        mask = (conf > lo) & (conf <= hi)
        if mask.any():
            rows.append({"bin_mid": (lo + hi) / 2, "accuracy": correct[mask].mean(), "confidence": conf[mask].mean(), "n": int(mask.sum())})
    return pd.DataFrame(rows)


